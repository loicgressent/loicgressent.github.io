var canvas = document.querySelector('.canva1');
var context = canvas.getContext('2d');
var grd = context.createLinearGradient(0,0,500,0);
grd.addColorStop(0,"#32fff4");
grd.addColorStop(1,"#eee");

    context.scale(0.2,0.2); 
    context.beginPath();
    context.fillStyle = grd;
    context.moveTo(190, 25);
    context.quadraticCurveTo(300, 87, 170, 300);
    context.quadraticCurveTo(50, 87, 160, 25);
    context.closePath();
    context.fill();

    context.fillStyle = '#ddd';
    context.fillRect(150, 15, 50, 20);

